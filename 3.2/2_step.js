/**
 * Создает функцию, которая возвращает функцию, использующую замыкание для
 * выполнения повторяемого сложения матрицы 2x2.
 *
 * @param {number[][]} addMatrix матрица 2x2 для сложения в формате [[a, b], [c, d]]
 *
 * @returns {function} функция, которая принимает матрицу 2x2, возвращает
 *  результат сложения матриц в формате [[a, b], [c, d]]
 */
function addMatrix2d(baseMatrix) {
  // Возвращаем функцию, которая "запоминает" baseMatrix через замыкание
  return function (matrixToAdd) {
    // Создаем результирующую матрицу 2x2
    const result = [
      [0, 0],
      [0, 0],
    ];

    // Выполняем поэлементное сложение
    for (let i = 0; i < 2; i++) {
      for (let j = 0; j < 2; j++) {
        result[i][j] = baseMatrix[i][j] + matrixToAdd[i][j];
      }
    }

    return result;
  };
}
